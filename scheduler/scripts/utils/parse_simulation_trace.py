def get_bs_pattern(model, num_epochs, batch_size):
    # print((model, num_epochs, batch_size))
    regular_mode_bs = [batch_size] * num_epochs

    if (
        (model == "Transformer" and batch_size == 128)
        or (model == "LM" and batch_size == 80)
        or (model == "ResNet-18" and batch_size == 256)
        or (model == "ResNet-50" and batch_size == 128)
        or (model == "Recommendation" and batch_size == 8192)
    ):
        return regular_mode_bs

    critical_regime = []

    if model == "ResNet-18":
        if batch_size in [16, 32, 64, 128]:
            regime = 10
        elif batch_size in [256]:
            regime = 20
        critical_regime = list(range(regime)) + list(range(150, 160))
    elif model == "ResNet-50":
        if batch_size in [16, 32, 64, 128]:
            regime = 10
        critical_regime = list(range(regime)) + list(range(30, 40))
    elif model == "LM":
        regime = 10
        critical_regime = list(range(regime))
    elif model == "Transformer":
        return regular_mode_bs
    elif model == "Recommendation":
        if batch_size in [512, 1024]:
            regime = 30
        elif batch_size in [2048]:
            regime = 40
        elif batch_size in [4096, 8192]:
            regime = 10
        # ???
        critical_regime = list(range(regime))

    for (epoch, bs) in enumerate(regular_mode_bs):
        if epoch not in critical_regime:
            regular_mode_bs[epoch] = 2 * bs

    return regular_mode_bs


